import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const styleModifiers: Record<string, string> = {
  "realistic": "photorealistic, natural lighting, high detail, 8K resolution",
  "cinematic": "cinematic film still, dramatic lighting, movie-quality, anamorphic lens",
  "creative": "artistic interpretation, bold composition, creative lighting, visually striking",
  "dynamic": "action shot, motion blur, energetic composition, dramatic angles",
  "cartoonish": "animated style, clean lines, vibrant but not oversaturated colors, playful",
  "sketch": "pencil sketch, hand-drawn, detailed linework, artistic rendering",
  "watercolor": "watercolor painting, soft edges, flowing colors, artistic",
  "oil-painting": "oil painting, classical brushwork, rich textures, fine art style",
  "hyper-real-4k": "ultra-realistic, 4K UHD, extreme detail, perfect clarity",
  "bokeh": "shallow depth of field, bokeh background, focused subject, professional photography",
  "fashion-editorial": "high-fashion photography, editorial style, elegant composition, luxury aesthetic",
  "minimal": "minimalist composition, clean design, simple background, elegant negative space",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { prompt, style } = await req.json();
    
    if (!prompt || typeof prompt !== "string") {
      return new Response(
        JSON.stringify({ error: "Prompt is required" }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      console.error("LOVABLE_API_KEY is not configured");
      return new Response(
        JSON.stringify({ error: "AI service not configured" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    // Build the enhanced prompt with style modifiers
    const styleModifier = styleModifiers[style] || styleModifiers["realistic"];
    const enhancedPrompt = `${prompt}. Style: ${styleModifier}. High quality, professional composition, 16:9 aspect ratio.`;

    console.log(`Generating image with style: ${style}`);
    console.log(`Enhanced prompt: ${enhancedPrompt.substring(0, 100)}...`);

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash-image-preview",
        messages: [
          {
            role: "user",
            content: enhancedPrompt
          }
        ],
        modalities: ["image", "text"]
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI usage credits exhausted. Please add credits to continue." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      
      return new Response(
        JSON.stringify({ error: "Failed to generate image" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const data = await response.json();
    const imageUrl = data.choices?.[0]?.message?.images?.[0]?.image_url?.url;
    
    if (!imageUrl) {
      console.error("No image in response:", JSON.stringify(data).substring(0, 500));
      return new Response(
        JSON.stringify({ error: "No image generated" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    console.log("Image generated successfully");

    return new Response(
      JSON.stringify({ imageUrl }),
      { headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );

  } catch (error) {
    console.error("Error in generate-image:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
